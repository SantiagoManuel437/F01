numero1 = 5 # el numero 5 es el valor con el que se define el numero1
numero2 = 3 # el numero 3 es el valor con el que se define el numero 2


suma = numero1 + numero2 # se define la operacion de suma con el valor de la suma del numero1 y numero2
resta = numero1 - numero2 # se define la operacion de resta con el valor de la resta del numero1 y numero2
multiplicacion = numero1 * numero2 # se define la operacion de multiplicacion con el valor de la multiplicacion del numero1 y numero2
division = numero1 / numero2 # se define la operacion de division con el valor de la division del numero1 y numero2


print("Suma:", suma) #se imprime en la consola la palabra "Suma" acompañado de ":" seguido se imprime el valor de suma
print("Resta:", resta) #se imprime en la consola la palabra "resta" acompañado de ":" seguido se imprime el valor de resta
print("Multiplicación:", multiplicacion) #se imprime en la consola la palabra "multiplicacion" acompañado de ":" seguido se imprime el valor de multiplicacion
print("División:", division) #se imprime en la consola la palabra "division" acompañado de ":" seguido se imprime el valor de division
